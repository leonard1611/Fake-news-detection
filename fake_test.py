class FakeNewsBERT(nn.Module):

    def __init__(self, meta_vocab_sizes, embedding_dim=32):

        super().__init__()

        # --- Branche Texte (BERT) ---
        self.bert = BertModel.from_pretrained("bert-base-uncased")

        # --- Branche Métadonnées (Embeddings) ---
        # meta_vocab_sizes est un dictionnaire {nom_colonne: taille_vocab}
        self.meta_embeddings = nn.ModuleDict({
            col: nn.Embedding(vocab_size, embedding_dim)
            for col, vocab_size in meta_vocab_sizes.items()
        })

        # --- Fusion et Classification ---
        # Dimension d'entrée du classifieur = BERT (768) + somme des embedding_dims
        total_meta_dim = len(meta_vocab_sizes) * embedding_dim
        classifier_input_dim = 768 + total_meta_dim+1 # pour la crédibilité

        self.classifier = nn.Sequential(
            nn.Linear(classifier_input_dim,512),
            nn.ReLU(),
            nn.Dropout(0.4),

            nn.Linear(512,128),
            nn.ReLU(),
            nn.Dropout(0.3),

            nn.Linear(128,1)
        )

    def forward(self, input_ids, attention_mask, meta_data,credibility):

        outputs = self.bert(
            input_ids=input_ids,
            attention_mask=attention_mask,
            return_dict=False
        )

        token_embeddings = outputs[0]
        attention_mask_expanded = attention_mask.unsqueeze(-1).expand(token_embeddings.size()).float()

        sum_embeddings = torch.sum(token_embeddings * attention_mask_expanded, 1)
        sum_mask = torch.clamp(attention_mask_expanded.sum(1), min=1e-9)

        cls_embedding = sum_embeddings / sum_mask
                
        # 2. Obtenir et concaténer les embeddings des métadonnées
        meta_embeds = []
        for i, (col, embed_layer) in enumerate(self.meta_embeddings.items()):
            # meta_data[:, i] extrait la i-ème colonne (la feature correspondante)
            encoded_feature = meta_data[:, i]
            meta_embeds.append(embed_layer(encoded_feature)) # [batch_size, embedding_dim]
            
        # Concaténer tous les embeddings de métadonnées
        meta_cat = torch.cat(meta_embeds, dim=1) # [batch_size, total_meta_dim]
        
        # 3. Fusionner Texte + Métadonnées
        combined_input = torch.cat((cls_embedding, meta_cat, credibility), dim=1) # [batch_size, classifier_input_dim]
        
        # 4. Classification
        return self.classifier(combined_input)


