# --- Fonction pour préparer les métadonnées ---
def prepare_metadata_encoders(df_train, columns_to_encode):
    encoders = {}
    for col in columns_to_encode:
        le = LabelEncoder()
        # On entraîne l'encodeur uniquement sur le train pour éviter le "data leakage"
        # Ajout d'une classe '<UNKNOWN>' pour gérer les valeurs inconnues
        unique_values = df_train[col].fillna('<UNKNOWN>').unique().tolist()
        if '<UNKNOWN>' not in unique_values:
            unique_values.append('<UNKNOWN>')
        le.fit(unique_values)
        encoders[col] = le
    return encoders

def encode_metadata(df, encoders, columns_to_encode):
    encoded_cols = []
    for col in columns_to_encode:
        le = encoders[col]
        # Mapper les valeurs, remplacer les inconnues par '<UNKNOWN>' avant transformation
        series = df[col].fillna('<UNKNOWN>')
        # Gérer les valeurs inconnues non vues lors de l'entraînement
        series = series.apply(lambda x: x if x in le.classes_ else '<UNKNOWN>')
        encoded_cols.append(torch.tensor(le.transform(series), dtype=torch.long))
    
    # Empiler les colonnes pour créer un tenseur [nb_echantillons, nb_features]
    return torch.stack(encoded_cols, dim=1)

